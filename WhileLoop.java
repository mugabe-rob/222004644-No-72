public class WhileLoop {
    public static void main(String[] args) {
        int i = 1; // Initializing a counter variable
        
        while (i <= 10) { // While the counter is less than or equal to 10
            System.out.println("Count: " + i); // Print the current value of the counter
            i++; // Increment the counter by 1
        }
    }
}
